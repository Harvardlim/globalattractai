// 月支索引到农历月份的映射
// 寅(2)=正月, 卯(3)=二月, 辰(4)=三月, 巳(5)=四月, 午(6)=五月, 未(7)=六月
// 申(8)=七月, 酉(9)=八月, 戌(10)=九月, 亥(11)=十月, 子(0)=十一月, 丑(1)=十二月
export function monthBranchToLunarMonth(branchIdx: number): number {
  const map: Record<number, number> = {
    2: 1, 3: 2, 4: 3, 5: 4, 6: 5, 7: 6,
    8: 7, 9: 8, 10: 9, 11: 10, 0: 11, 1: 12,
  };
  return map[branchIdx] ?? 1;
}

