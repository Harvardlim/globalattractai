export interface HexagramInfo {
  name: string;
  meaning: string;
  upperTrigram: string;
  lowerTrigram: string;
  upperSymbol: string;
  lowerSymbol: string;
  upperLabel: string;
  lowerLabel: string;
}

/**
 * 门宫卦：门(上卦) + 宫(下卦)
 */
export function getDoorPalaceHexagram(door: string, palaceId: number): HexagramInfo | null {
  const upperIdx = DOOR_TRIGRAM[door];
  const lowerIdx = PALACE_TRIGRAM[palaceId];
  if (upperIdx === undefined || lowerIdx === undefined) return null;

  const name = HEXAGRAM_TABLE[upperIdx][lowerIdx];
  return {
    name,
    meaning: HEXAGRAM_MEANING[name] || '',
    upperTrigram: TRIGRAM_NAMES[upperIdx],
    lowerTrigram: TRIGRAM_NAMES[lowerIdx],
    upperSymbol: TRIGRAM_SYMBOLS[upperIdx],
    lowerSymbol: TRIGRAM_SYMBOLS[lowerIdx],
    upperLabel: door,
    lowerLabel: `${TRIGRAM_NAMES[lowerIdx]}宫`,
  };
}

/**
 * 星门卦：星(上卦) + 门(下卦)
 */
export function getStarDoorHexagram(star: string, door: string): HexagramInfo | null {
  const upperIdx = STAR_TRIGRAM[star];
  const lowerIdx = DOOR_TRIGRAM[door];
  if (upperIdx === undefined || lowerIdx === undefined) return null;

  const name = HEXAGRAM_TABLE[upperIdx][lowerIdx];
  return {
    name,
    meaning: HEXAGRAM_MEANING[name] || '',
    upperTrigram: TRIGRAM_NAMES[upperIdx],
    lowerTrigram: TRIGRAM_NAMES[lowerIdx],
    upperSymbol: TRIGRAM_SYMBOLS[upperIdx],
    lowerSymbol: TRIGRAM_SYMBOLS[lowerIdx],
    upperLabel: star,
    lowerLabel: door,
  };
}

